    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        var base_ = Double.parseDouble(txtBase.getText());
        var altura_ = Double.parseDouble(txtAltura.getText());
        var apotema_ = Double.parseDouble(txtApotema.getText());
        
        Piramide piramide = new Piramide(base_, altura_, apotema_);
        
        piramide.calcularVolumen();
        piramide.calcularSuperficie();
        
        volumen.setText("Volumen (cm3) = " + String.format("%.2f", piramide.calcularVolumen()));
        superficie.setText("Superficie (cm2) = " + String.format("%.2f", piramide.calcularSuperficie()));
    }//GEN-LAST:event_btnCalcularActionPerformed

